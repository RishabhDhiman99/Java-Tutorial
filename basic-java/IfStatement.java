package tutorial;

public class IfStatement {
	public static void main(String args[]) {
		int num = 7;
		// if statement
		if(num == 7) {
			// if test true execute this
			System.out.println("num is 7");
		}
		else {
			// else this will be executed
			System.out.println("num is not 7");
		}
	}

}
