package tutorial;

public class Variables {
	public static void main(String args[]) {
		// A variable is nothing but placeholder for a value.
		int a = 5; // integer data type.
		// while using float data type append 'f' to value to tell it belongs to
		// float type otherwise java assumes it to be a double type.
		float b = 3.14f; // floating point data type.
		double c = 4.1222;
		boolean d = true; // boolean value ie true or false
		char e = 'a'; // character constant 
		String f = "Hello"; // string data type
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
}
